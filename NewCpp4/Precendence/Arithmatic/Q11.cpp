/*
11. Combining Modulo with Arithmetic:
Problem: Evaluate the following expression:
k = 20 % 6 + 4 * 2;
Question: What is the value of k?
*/
#include<iostream>
using namespace std;
int main(){
    int k = 20 % 6 + 4 * 2;
    cout<<k;
}
/*
Modulo:
20 % 6 -> 2
Multiplication:
4 * 2 -> 8
Addition:
2 + 8 -> 10
*/